package meshes

// N200Type is used by several 200 responses for "type" fields
type N200Type = string

// N201EventType is the event type on creation
type N201EventType = string

// N201RuleType is the rule type on creation
type N201RuleType = string

// N200Status is used by several 200 responses for "status" fields
type N200Status = string

// N200Role is used by several 200 responses for "role" fields
type N200Role = string

// N200RecordsType is the type field on records in list responses
type N200RecordsType = string

// N200RecordsStatus is the status field on event records
type N200RecordsStatus = string

// N200IntegrationType is the integration_type on single-item responses
type N200IntegrationType = string

// N200MappingIntegrationType is the integration_type on mapping responses
type N200MappingIntegrationType = string

// N200RuleEventsIntegrationType is integration_type on rule_events in single event responses
type N200RuleEventsIntegrationType = string

// N200RuleEventsStatus is status on rule_events in single event responses
type N200RuleEventsStatus = string

// N200RuleEventsType is type on rule_events in single event responses
type N200RuleEventsType = string

// N200RecordsRuleEventsIntegrationType is integration_type on rule_events in list responses
type N200RecordsRuleEventsIntegrationType = string

// N200RecordsRuleEventsStatus is status on rule_events in list responses
type N200RecordsRuleEventsStatus = string

// N200RecordsRuleEventsType is type on rule_events in list responses
type N200RecordsRuleEventsType = string

// N200FieldsType is the type field on connection fields
type N200FieldsType = string

// N200FieldsConstraintsFormat is the format on field constraints
type N200FieldsConstraintsFormat = string
